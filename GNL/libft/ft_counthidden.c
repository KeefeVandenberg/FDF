#include "libft.h"

int	ft_counthidden(char s)
{
	int hidden;

	hidden = 0;
	if (s == ' ' || s == '\t' || s == '\n' || s == '\v'
		|| s == '\f' || s == '\r')
			hidden++;
	return (hidden);
}
